    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    	jLabel3.setText("");
    	String plaintext = jTextArea2.getText();
    	int a = Integer.parseInt(jTextField1.getText());
    	int b = Integer.parseInt(jTextField2.getText());
    	try {
			jTextArea1.setText(affine.encrypt(plaintext, a, b));
		} catch (NotCoprimeException e) {
			jLabel3.setText("a is not coprime with the number of digits in devnagri(=128)");
			e.printStackTrace();
		} catch (OutOfBoundsException e) {
			jLabel3.setText("a and/or b not in range.");
			e.printStackTrace();
		}
    }//GEN-LAST:event_jButton1ActionPerformed


