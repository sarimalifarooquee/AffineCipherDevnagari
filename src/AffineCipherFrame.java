    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    	jLabel3.setText("");
    	jTextArea1.setText("");
    	String ciphertext = jTextArea2.getText();
    	int numCharsConsider = jSlider2.getValue();
    	SortedSet<Solution> solutions = affine.breakCipher(ciphertext, numCharsConsider);
       	jTextArea1.append(String.format("  %c ,  %c    %-14s %-25s%n", 'a', 'b',"fitness", "plaintext snapshot"));
    	jTextArea1.append("------------------------------------------------\n\r");
 		int scounter = 0;
		for(Solution s: solutions){
			if(scounter == 5) break; //DISPLAY ONLY TOP 5 POSSIBLE SOLUTIONS
			jTextArea1.append(s.getSnapshot()+"\n\r");
			scounter++;
		}


        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

